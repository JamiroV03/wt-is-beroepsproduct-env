<h1>Privacyverklaring</h1>

<p>
Pizzeria Sole Machina hecht veel waarde aan de bescherming van jouw persoonsgegevens.
In deze privacyverklaring leggen wij uit welke gegevens wij verzamelen en hoe wij hiermee omgaan.
</p>

<h2>Welke gegevens verzamelen wij?</h2>
<ul>
    <li>Gebruikersnaam</li>
    <li>Naam</li>
    <li>Adresgegevens</li>
    <li>Bestelgegevens</li>
</ul>

<h2>Waarvoor gebruiken wij deze gegevens?</h2>
<p>
De gegevens worden uitsluitend gebruikt voor:
</p>
<ul>
    <li>Het verwerken en bezorgen van bestellingen</li>
    <li>Het tonen van bestelgeschiedenis aan de gebruiker</li>
    <li>Het beheren van bestellingen door personeel</li>
</ul>

<h2>Beveiliging</h2>
<p>
Wij nemen passende technische maatregelen om persoonsgegevens te beveiligen.
Wachtwoorden worden versleuteld opgeslagen en invoer wordt gevalideerd om misbruik te voorkomen.
</p>

<h2>Delen van gegevens</h2>
<p>
Persoonsgegevens worden niet gedeeld met derden.
</p>

<h2>Rechten van gebruikers</h2>
<p>
Gebruikers hebben het recht om hun gegevens in te zien, aan te passen of te laten verwijderen.
</p>

<p>
Voor vragen over deze privacyverklaring kun je contact opnemen met Pizzeria Sole Machina.
</p>

